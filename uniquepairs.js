const sum = (nums, target) => {
    const result = []
    const map = new Map()
    const seen = new Set()

    for (let i = 0; i < nums.length; i++) {
        const complement = target - nums[i]

        if(map.has(complement)) { 
            const sortedPair = [complement, nums[i]].sort((a, b) => a - b)
            const pairKey = sortedPair.join('')
            

            if(!seen.has(pairKey)) {
                result.push(sortedPair)
            } 

            seen.add(pairKey)
            
        } else {
            map.set(nums[i], i)
        }
    }

    return result
}

