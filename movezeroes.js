// Output [1, 3, 12, 0, 0]

const move = (nums) => {
    let k = 0

    for(let i = 0; i < nums.length; i++) {
        if(nums[i] !== 0) {
            nums[k] = nums[i]
            k++
        }
    }

    for(j = k; j < nums.length; j++) {
        nums[j] = 0
    }

    return nums
}

